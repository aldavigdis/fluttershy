$(document).on('ready page:load', function () {
	if (window.location.pathname == '/mobile_usage') {
		chartMinutes('.mobile-calls-chart svg', window.mobileMinutes)
		$('.usage-data-container:not(.mobile-calls)').hide()
		$('.chart-select a').click(function(e){
			e.preventDefault()
			selectedChart = $(this).attr('href')
			$('.usage-data-container:not('+selectedChart+')').hide()
			$(selectedChart).show()
			$('.chart-select a[href='+selectedChart+']').parent().addClass('active')
			$('.chart-select a:not(a[href='+selectedChart+'])').parent().removeClass('active')
			switch (selectedChart) {
			case '#mobile-calls':
				chartMinutes('.mobile-calls-chart svg', window.mobileMinutes)
				break
			case '#mobile-data':
				chartData('.mobile-data-chart svg', window.dataUsage)
				break;
			case '#mobile-messages':
				chartInterger('.mobile-messages-chart svg', window.mobileMessages)
				break;
			}
		})
	}
});

